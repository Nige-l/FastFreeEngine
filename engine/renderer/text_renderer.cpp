// Font atlas layout: 16 columns x 6 rows = 96 cells
// Atlas pixel dimensions: 128 x 48
static constexpr u32 FONT_ATLAS_COLS = 16;
static constexpr u32 FONT_ATLAS_ROWS = 6;
static constexpr u32 FONT_GLYPH_W   = 8;
static constexpr u32 FONT_GLYPH_H   = 8;
static constexpr u32 ATLAS_W        = FONT_ATLAS_COLS * FONT_GLYPH_W; // 128
static constexpr u32 ATLAS_H        = FONT_ATLAS_ROWS * FONT_GLYPH_H; // 48

void initTextRenderer(TextRenderer& tr, const f32 screenWidth, const f32 screenHeight) {
    tr.screenWidth  = screenWidth;
    tr.screenHeight = screenHeight;
    tr.glyphCount   = 0;

    // Allocate glyph buffer (persistent, not per-frame)
    tr.glyphs = static_cast<GlyphQuad*>(
        std::malloc(MAX_TEXT_GLYPHS * sizeof(GlyphQuad)));

    // Bake the 8x8 font into an RGBA8 texture atlas.
    // White pixels with alpha=255 for set bits, alpha=0 for unset bits.
    static constexpr u32 ATLAS_PIXELS = ATLAS_W * ATLAS_H;
    u8 pixelData[ATLAS_PIXELS * 4]; // RGBA8
    std::memset(pixelData, 0, sizeof(pixelData));

    for (u32 charIdx = 0; charIdx < 95; ++charIdx) {
        const u32 col = charIdx % FONT_ATLAS_COLS;
        const u32 row = charIdx / FONT_ATLAS_COLS;

        for (u32 py = 0; py < FONT_GLYPH_H; ++py) {
            const u8 rowBits = FONT_8X8[charIdx][py];
            for (u32 px = 0; px < FONT_GLYPH_W; ++px) {
                const bool lit = (rowBits >> (7 - px)) & 1;
                const u32 atlasX = col * FONT_GLYPH_W + px;
                const u32 atlasY = row * FONT_GLYPH_H + py;
                const u32 offset = (atlasY * ATLAS_W + atlasX) * 4;
                pixelData[offset + 0] = 255; // R
                pixelData[offset + 1] = 255; // G
                pixelData[offset + 2] = 255; // B
                pixelData[offset + 3] = lit ? 255 : 0; // A
            }
        }
    }

    // Fill cell 95 (unused by font) as a solid white block for drawRect().
    {
        const u32 col = 95 % FONT_ATLAS_COLS; // col=15
        const u32 row = 95 / FONT_ATLAS_COLS; // row=5
        for (u32 py = 0; py < FONT_GLYPH_H; ++py) {
            for (u32 px = 0; px < FONT_GLYPH_W; ++px) {
                const u32 atlasX = col * FONT_GLYPH_W + px;
                const u32 atlasY = row * FONT_GLYPH_H + py;
                const u32 offset = (atlasY * ATLAS_W + atlasX) * 4;
                pixelData[offset + 0] = 255;
                pixelData[offset + 1] = 255;
                pixelData[offset + 2] = 255;
                pixelData[offset + 3] = 255;
            }
        }
    }

    rhi::TextureDesc desc;
    desc.width     = ATLAS_W;
    desc.height    = ATLAS_H;
    desc.format    = rhi::TextureFormat::RGBA8;
    desc.filter    = rhi::TextureFilter::NEAREST; // Crisp pixel font
    desc.wrap      = rhi::TextureWrap::CLAMP_TO_EDGE;
    desc.pixelData = pixelData;
    tr.fontAtlas   = rhi::createTexture(desc);

    if (!rhi::isValid(tr.fontAtlas)) {
        FFE_LOG_ERROR("TextRenderer", "Failed to create font atlas texture");
    } else {
        FFE_LOG_INFO("TextRenderer", "Font atlas created: %ux%u, 95 glyphs", ATLAS_W, ATLAS_H);
    }
}

void shutdownTextRenderer(TextRenderer& tr) {
    if (rhi::isValid(tr.fontAtlas)) {
        rhi::destroyTexture(tr.fontAtlas);
        tr.fontAtlas = {};
    }

    // Unload all TTF fonts.
    for (u32 i = 0; i < MAX_FONTS; ++i) {
        if (tr.fonts[i].occupied) {
            unloadFont(tr, i + 1);
        }
    }

    std::free(tr.glyphs);
    tr.glyphs     = nullptr;
    tr.glyphCount = 0;
}

void beginText(TextRenderer& tr) {
    tr.glyphCount = 0;
}

void drawText(TextRenderer& tr, const char* text,
              f32 x, f32 y, const f32 scale,
              const f32 r, const f32 g, const f32 b, const f32 a) {
    if (text == nullptr) return;

    const f32 glyphW = static_cast<f32>(FONT_GLYPH_W) * scale;
    const f32 glyphH = static_cast<f32>(FONT_GLYPH_H) * scale;
    const f32 uvW    = 1.0f / static_cast<f32>(FONT_ATLAS_COLS);
    const f32 uvH    = 1.0f / static_cast<f32>(FONT_ATLAS_ROWS);

    while (*text != '\0' && tr.glyphCount < MAX_TEXT_GLYPHS) {
        const char ch = *text++;

        // Newline support
        if (ch == '\n') {
            x = 0.0f; // Reset to left (caller can offset if needed)
            y += glyphH;
            continue;
        }

        // Skip unprintable characters
        if (ch < 32 || ch > 126) continue;

        const u32 charIdx = static_cast<u32>(ch - 32);
        const u32 col = charIdx % FONT_ATLAS_COLS;
        const u32 row = charIdx / FONT_ATLAS_COLS;

        GlyphQuad& gq = tr.glyphs[tr.glyphCount++];
        gq.x      = x;
        gq.y      = y;
        gq.width  = glyphW;
        gq.height = glyphH;
        gq.uvMinX = static_cast<f32>(col) * uvW;
        gq.uvMinY = static_cast<f32>(row) * uvH;
        gq.uvMaxX = gq.uvMinX + uvW;
        gq.uvMaxY = gq.uvMinY + uvH;
        gq.r = r;
        gq.g = g;
        gq.b = b;
        gq.a = a;
        gq.texture = {}; // id=0 means bitmap font atlas

        x += glyphW;
    }
}

void drawRect(TextRenderer& tr,
              const f32 x, const f32 y, const f32 width, const f32 height,
              const f32 r, const f32 g, const f32 b, const f32 a) {
    if (tr.glyphCount >= MAX_TEXT_GLYPHS) return;

    // Cell 95 is the solid white block
    const f32 uvW = 1.0f / static_cast<f32>(FONT_ATLAS_COLS);
    const f32 uvH = 1.0f / static_cast<f32>(FONT_ATLAS_ROWS);
    const u32 col = 95 % FONT_ATLAS_COLS;
    const u32 row = 95 / FONT_ATLAS_COLS;

    GlyphQuad& gq = tr.glyphs[tr.glyphCount++];
    gq.x      = x;
    gq.y      = y;
    gq.width  = width;
    gq.height = height;
    gq.uvMinX = static_cast<f32>(col) * uvW;
    gq.uvMinY = static_cast<f32>(row) * uvH;
    gq.uvMaxX = gq.uvMinX + uvW;
    gq.uvMaxY = gq.uvMinY + uvH;
    gq.r = r;
    gq.g = g;
    gq.b = b;
    gq.a = a;
    gq.texture = {}; // id=0 means bitmap font atlas
}

void flushText(TextRenderer& tr, SpriteBatch& batch,
               rhi::SpriteVertex* staging) {
    if (tr.glyphCount == 0) return;

    // Switch to screen-space orthographic projection.
    // Origin is top-left: (0,0) = top-left, (screenW, screenH) = bottom-right.
    const glm::mat4 screenOrtho = glm::ortho(
        0.0f, tr.screenWidth,
        tr.screenHeight, 0.0f,  // flipped Y: top=0, bottom=screenH
        -1.0f, 1.0f);
    rhi::setViewProjection(screenOrtho);

    beginSpriteBatch(batch, staging);

    for (u32 i = 0; i < tr.glyphCount; ++i) {
        const GlyphQuad& gq = tr.glyphs[i];

        SpriteInstance sprite;
        // SpriteInstance position is the CENTER of the quad
        sprite.position = {gq.x + gq.width * 0.5f, gq.y + gq.height * 0.5f};
        sprite.size     = {gq.width, gq.height};
        sprite.uvMin    = {gq.uvMinX, gq.uvMinY};
        sprite.uvMax    = {gq.uvMaxX, gq.uvMaxY};
        sprite.color    = {gq.r, gq.g, gq.b, gq.a};
        sprite.rotation = 0.0f;
        sprite.depth    = 0.0f;

        // Use per-glyph texture if set (TTF fonts), otherwise bitmap atlas.
        const rhi::TextureHandle tex = rhi::isValid(gq.texture) ? gq.texture : tr.fontAtlas;
        addSprite(batch, tex, sprite);
    }

    endSpriteBatch(batch);
}

